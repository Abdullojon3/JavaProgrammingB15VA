package day03_primitives_mathoperators;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Hello\tB15");//Hello	B15
        System.out.println("Hello\t\tB15 \tFriends");//Hello		B15 Friends

        //I love "Java" programming
        System.out.println("I love \"Java\" programming");

        //Lets buy "coffee" and "tea"
        System.out.println("Lets buy \"coffee\" and \"tea\"");

        //Java
        //Selenium
        System.out.println("Java");
        System.out.println("Selenium");

        System.out.println("Java\nSelenium");

        //   / | \
        System.out.println("/ | \\");

        //Print this: "\_/"

        System.out.println("\"\\_/\"");











    }
}
